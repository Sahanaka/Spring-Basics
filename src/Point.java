public class Point
{
    private int xcor; // Cartesian coordinates x & y
    private int ycor;

    public int getXcor()
    {
        return xcor;
    }

    public void setXcor(int xcor)
    {
        this.xcor = xcor;
    }

    public int getYcor()
    {
        return ycor;
    }

    public void setYcor(int ycor)
    {
        this.ycor = ycor;
    }
}
